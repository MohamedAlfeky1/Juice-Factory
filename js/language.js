let currentLang = localStorage.getItem("lang") || document.documentElement.lang;

document.documentElement.lang = localStorage.getItem("lang");

document.getElementById("lang").addEventListener("click", () => {
  toggleLanguage();
  location.reload();
});

if (currentLang === "ar") {
  var linkTag = document.createElement("link");
  linkTag.rel = "stylesheet";
  linkTag.href = "css/styles-rtl.css";
  document.head.appendChild(linkTag);
}

function updateText(language) {
  const allEle = document.querySelectorAll("[data-text]");
  allEle.forEach((element) => {
    const translationKey = element.getAttribute("data-text");
    element.textContent = languages[language][translationKey];
  });
  localStorage.setItem("lang", language);
}

function toggleLanguage() {
  currentLang = currentLang === "en" ? "ar" : "en";
  updateText(currentLang);
}

updateText(currentLang);
